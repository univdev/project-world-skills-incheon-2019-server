    <script
    src="https://code.jquery.com/jquery-3.4.1.js"
    integrity="sha256-WpOohJOqMqqyKL9FccASB9O0KwACQJpFTUBLTYOVvVU="
    crossorigin="anonymous"></script>
    <script>
        function on(event, target, callback) {
            $(document).on(event, target, callback);
        }

        (() => {
            var reservations = [];
            var placements = [];

            $.get('/data/reservation.json').then((data) => {
                reservations = data.reservations;
                $.get('/data/placement.json').then((data) => {
                    placements = data.places;
                    ReservationListManager.draw(ReservationDataManager.sort(reservations));
                });
            });

            var ReservationDataManager = {
                sort(reservations) {
                    return reservations.sort((a, b) => new Date(a.since) > new Date(b.since) ? 1 : new Date(a.since) < new Date(b.since) ? -1 : 0);
                },
            };

            var PlacementDataManager = {
                get(index) {
                    return placements.filter((data) => data.id === index)[0];
                },
            };

            var Slide = {
                position: 0,
                interval: null,
                target: '.animation__movement',
                duration: 2000,
                clock: 5000,
                button: '.animation__page-button',
                prevButton: '.animation__slide-arrow-left',
                nextButton: '.animation__slide-arrow-right',
                move(n) {
                    this.clear();
                    var index = this.filter(n);
                    var position = -100 * index;

                    this.activeButton(index);
                    
                    $(this.target).animate({
                        left: `${position}%`,
                    }, this.duration, () => {
                        this.interval = setTimeout(() => { this.move(index + 1) }, this.clock);
                    });

                    this.position = index;
                },
                filter(n) {
                    var children = $(this.target).find('.animation__slide-child');

                    return n >= children.length ? 0 : n < 0 ? children.length - 1 : n;
                },
                clear() {
                    clearTimeout(this.interval);
                    $(this.target).clearQueue().stop();
                },
                activeButton(index) {
                    $(this.button).removeClass('--active');
                    $($(this.button)[index]).addClass('--active');
                },
                next() {
                    this.move(this.position + 1);
                },
                prev() {
                    this.move(this.position - 1);
                },
            };

            Slide.move(0);

            on('click', Slide.button, (e) => {
                var index = $(e.target).index();
                Slide.move(index);
            });
            on('click', Slide.prevButton, () => { Slide.prev() });
            on('click', Slide.nextButton, () => { Slide.next() });

            var Tooltip = {
                target: $('.tooltip'),
                init() {
                    this.target.hide();
                },
                move(x, y) {
                    this.toggle(true);
                    this.target.css({
                        'left': x,
                        'top': y,
                    });
                },
                toggle(flag) {
                    var target = this.target;

                    if (flag) target.show();
                    else target.hide();
                },
                draw(placement) {
                    this.target.find('.tooltip__image').css('background-image', `url(/images/${placement.image})`);
                    this.target.find('.tooltip__title').text(placement.name);
                },
            };

            Tooltip.init();

            on('mouseenter', '.festival__frame', (e) => {
                var column = $(e.currentTarget).parent().parent().parent('.festival__column');
                var placementId = column.data('placement');
                var placement = PlacementDataManager.get(placementId);
                Tooltip.toggle(true);
                Tooltip.draw(placement);
            })
            on('mousemove', '.festival__frame', (e) => {
                var x = e.pageX + 5;
                var y = e.pageY + 5;
                Tooltip.move(x, y);
            });
            on('mouseleave', '.festival__frame', () => { Tooltip.toggle(false);})

            var Introduce = {
                target: '.introduce__frame-big',
                selector: '.introduce__frame-small',
                change(n) {
                    $(this.target).css('background-image', this.items(n));
                },
                items(n) {
                    var children = $(this.selector);
                    var index = children.length <= n ? 0 : n < 0 ? children.length - 1 : n;
                    return $(children[index]).css('background-image')
                },
            };

            Introduce.change(0);

            on('click', Introduce.selector, (e) => {
                var index = $(e.currentTarget).index();
                Introduce.change(index)
            });

            var ReservationListManager = {
                container: $('.festival__container'),
                column: $($('.festival__column').get(0)),
                draw(reservations) {
                    this.clear();
                    if (reservations.length <= 0) {
                        this.container.html('<h1 class="no-festival">현재 개최 예정인 축제가 없습니다.</h1>');
                    }
                    var data = reservations.slice(0, 8);
                    for (var i = 0; i < data.length; i += 1) {
                        var reservation = data[i];
                        var placement = PlacementDataManager.get(reservation.placement);
                        var clone = this.column.clone();
                        clone.attr('data-placement', placement.id);
                        clone.find('.festival__frame').css('background-image', `url(/images/${placement.image})`)
                        clone.find('.festival__title').text(reservation.name);
                        clone.find('.festival__start-date').text(`대여 시작일: ${reservation.since}`);
                        clone.find('.festival__end-date').text(`대여 종료일: ${reservation.until}`);
                        clone.find('.festival__created-at').text(reservation.createdAt);
                        this.container.append(clone);
                    }
                },
                clear() {
                    this.container.html('');
                },
            };

        })();
    </script>